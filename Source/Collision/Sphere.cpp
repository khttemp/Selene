using namespace Selene;
using namespace Collision;


//-----------------------------------------------------------------------------------
/**
*/
//-----------------------------------------------------------------------------------
CSphere::CSphere( void )
{
}


//-----------------------------------------------------------------------------------
/**
	@brief コンストラクタ
	@author 葉迩倭

	@param x		[in] Ｘ座標
	@param y		[in] Ｙ座標
	@param z		[in] Ｚ座標
	@param r		[in] 半径
*/
//-----------------------------------------------------------------------------------
CSphere::CSphere( Float x, Float y, Float z, Float r )
{
	this->vCenter.x = x;
	this->vCenter.y = y;
	this->vCenter.z = z;
	this->fRadius = r;
}


//-----------------------------------------------------------------------------------
/**
	@brief コンストラクタ
	@author 葉迩倭

	@param c		[in] 中心座標
	@param r		[in] 半径
*/
//-----------------------------------------------------------------------------------
CSphere::CSphere( const Math::Vector3D &c, Float r )
{
	this->vCenter = c;
	this->fRadius = r;
}

